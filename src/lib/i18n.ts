export function t(key: string, lang: LangCode): string {
  return translations[lang]?.[key] ?? translations.it[key] ?? key;
}

export function getDefaultLang(): LangCode {
  if (typeof window === "undefined") return "it";
  const stored = localStorage.getItem("rainbowmap-lang");
  if (stored && translations[stored as LangCode]) return stored as LangCode;
  const browserLang = navigator.language.slice(0, 2);
  if (translations[browserLang as LangCode]) return browserLang as LangCode;
  return "en";
}
